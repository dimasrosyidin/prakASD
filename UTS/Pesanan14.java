public class Pesanan14 {
    String kodePesanan;
    String namaPesanan;
    double harga;

    public Pesanan14(String kodePesanan, String namaPesanan, double harga) {
        this.kodePesanan = kodePesanan;
        this.namaPesanan = namaPesanan;
        this.harga = harga;
    }

    public String getKodePesanan() {
        return kodePesanan;
    }

    public String getNamaPesanan() {
        return namaPesanan;
    }

    public double getHarga() {
        return harga;
    }
}


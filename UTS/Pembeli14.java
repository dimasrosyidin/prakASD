public class Pembeli14 {
    private String nama;
    private String noHp;

    public Pembeli14(String nama, String noHp) {
        this.nama = nama;
        this.noHp = noHp;
    }

    public String getNama() {
        return nama;
    }

    public String getNoHp() {
        return noHp;
    }
}